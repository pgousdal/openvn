static unsigned int normalise_character(unsigned int character) {
    if (character < OPENVN_BITMAP_FONT_FIRST ||
        character > OPENVN_BITMAP_FONT_LAST) {
        return (unsigned int)'?';
    }
    return character;
}

int openvn_bitmap_font_glyph_pixel(
    unsigned int character,
    unsigned int x,
    unsigned int y
) {
    unsigned int index;
    unsigned char row;

    if (x >= OPENVN_BITMAP_FONT_GLYPH_WIDTH ||
        y >= OPENVN_BITMAP_FONT_GLYPH_HEIGHT) {
        return 0;
    }

    character = normalise_character(character);
    index = character - OPENVN_BITMAP_FONT_FIRST;
    row = OPENVN_BITMAP_FONT[index][y];
    return (row & (unsigned char)(0x80U >> x)) != 0U;
}

void openvn_bitmap_font_measure(
    const char *text,
    OpenVNBitmapFontMetrics *metrics
) {
    unsigned int column;
    unsigned int maximum;
    unsigned int lines;

    if (metrics == NULL) {
        return;
    }

    metrics->width = 0U;
    metrics->height = 0U;
    metrics->lines = 0U;

    if (text == NULL || text[0] == '\0') {
        return;
    }

    column = 0U;
    maximum = 0U;
    lines = 1U;

    while (*text != '\0') {
        if (*text == '\n') {
            if (column > maximum) {
                maximum = column;
            }
            column = 0U;
            ++lines;
        } else {
            column += OPENVN_BITMAP_FONT_ADVANCE;
        }
        ++text;
    }

    if (column > maximum) {
        maximum = column;
    }

    metrics->width = maximum;
    metrics->height = lines * OPENVN_BITMAP_FONT_LINE_HEIGHT;
    metrics->lines = lines;
}

int openvn_bitmap_font_draw(
    const char *text,
    int x,
    int y,
    OpenVNBitmapFontPlot plot,
    void *context
) {
    int cursor_x;
    int cursor_y;
    unsigned int glyph_x;
    unsigned int glyph_y;
    int pixels;

    if (text == NULL || plot == NULL) {
        return 0;
    }

    cursor_x = x;
    cursor_y = y;
    pixels = 0;

    while (*text != '\0') {
        if (*text == '\n') {
            cursor_x = x;
            cursor_y += (int)OPENVN_BITMAP_FONT_LINE_HEIGHT;
            ++text;
            continue;
        }

        for (glyph_y = 0U;
             glyph_y < OPENVN_BITMAP_FONT_GLYPH_HEIGHT;
             ++glyph_y) {
            for (glyph_x = 0U;
                 glyph_x < OPENVN_BITMAP_FONT_GLYPH_WIDTH;
                 ++glyph_x) {
                if (openvn_bitmap_font_glyph_pixel(
                        (unsigned int)(unsigned char)*text,
                        glyph_x,
                        glyph_y
                    )) {
                    plot(
                        context,
                        cursor_x + (int)glyph_x,
                        cursor_y + (int)glyph_y
                    );
                    ++pixels;
                }
            }
        }

        cursor_x += (int)OPENVN_BITMAP_FONT_ADVANCE;
        ++text;
    }

    return pixels;
}
